     */
    public function store(Request $request)
    {
        //crear un arreglod de producto para que almacene la informacion
        $producto = [
            [
                "id" => $request->prod_id, 
                "name" => $request->prod_name,
                "cantidad" => $request->cantidad
            ]
            ];
       //Crear una variable de session 
       session(['cart' => $producto]);
        
    }

